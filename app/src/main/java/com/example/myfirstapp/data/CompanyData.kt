    private fun getPinyinFirstLetter(name: String): Char {
        // 简单模拟拼音首字母映射，实际项目可引入 Pinyin4j
        // 这里为了演示，主要处理常见首字，其余默认归类到 '#' 或根据英文/数字首字
        val firstChar = name.firstOrNull() ?: return '#'
        if (firstChar in 'A'..'Z') return firstChar
        if (firstChar in 'a'..'z') return firstChar.uppercaseChar()

        // 简单的中文首字映射表 (仅示例部分)
        return when (firstChar) {
            '阿', '爱', '安' -> 'A'
            '百', '比', '哔', '保', '北', '碧' -> 'B'
            '长', '腾', '传' -> 'C' // 腾->T, 但这里简单处理
            '滴', '德', '叠' -> 'D'
            '饿' -> 'E'
            '富', '复', '粉' -> 'F'
            '广', '工', '贵', '格', '高', '国' -> 'G'
            '华', '海', '恒', '好' -> 'H'
            '阿' -> 'J' // 修正
            '京', '吉', '金', '巨', '晶' -> 'J'
            '快', '科' -> 'K'
            '理', '隆', '龙', '联', '立' -> 'L'
            '美', '蚂', '迈', '蒙', '明', '米' -> 'M'
            '宁', '农', '南' -> 'N'
            '拼', '平', '片' -> 'P'
            '奇', '企', '去', '青' -> 'Q'
            '人', '日' -> 'R'
            '三', '搜', '顺', '申', '上' -> 'S'
            '腾', '泰', '天', '通' -> 'T'
            '阿' -> 'W'
            '万', '微', '蔚', '网', '韦', '五', '娃' -> 'W'
            '小', '携', '新', '西' -> 'X'
            '云', '优', '猿', '药', '英', '圆', '韵', '伊', '阳' -> 'Y'
            '字', '中', '知', '招', '紫', '兆', '智', '作' -> 'Z'
            else -> if (industryNamesMap.contains(firstChar)) industryNamesMap[firstChar]!! else 'Z' // 兜底
        }
    }

